    private void isiDataAgenda() {
        try {
            // Format tanggal dari string ke Date
            java.text.SimpleDateFormat sdfTanggal = new java.text.SimpleDateFormat("yyyy-MM-dd");
            java.util.Date tgl = sdfTanggal.parse(dataAgenda.getTanggal());
            jDateChooserTanggal.setDate(tgl);

            // Format waktu ke spinner
            java.text.SimpleDateFormat sdfWaktu = new java.text.SimpleDateFormat("HH:mm");
            java.util.Date waktu = sdfWaktu.parse(dataAgenda.getWaktu());
            javax.swing.SpinnerDateModel model = new javax.swing.SpinnerDateModel();
            spnWaktu.setModel(model);
            spnWaktu.setValue(waktu);
            spnWaktu.setEditor(new javax.swing.JSpinner.DateEditor(spnWaktu, "HH:mm"));
            // Atur format tampilan waktu menjadi HH:mm
            JSpinner.DateEditor editor = new JSpinner.DateEditor(spnWaktu, "HH:mm");
            spnWaktu.setEditor(editor);
            // Ambil formatter bawaan dari editor
            javax.swing.JFormattedTextField tf = editor.getTextField();
            javax.swing.text.DefaultFormatter formatter = (javax.swing.text.DefaultFormatter) tf.getFormatter();

            // Atur agar tidak bisa mengetik karakter yang tidak valid
            formatter.setAllowsInvalid(false); // user tidak bisa input huruf/karakter aneh
            formatter.setOverwriteMode(true);  // mengganti karakter lama (bukan menyisipkan)

            // Set kegiatan dan keterangan
            txtKegiatan.setText(dataAgenda.getKegiatan());
            txtKeterangan.setText(dataAgenda.getKeterangan());

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Gagal memuat data: " + e.getMessage(), 
                "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

