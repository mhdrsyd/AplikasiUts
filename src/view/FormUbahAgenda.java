    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
try {
    // Membuat formatter untuk tanggal dengan format tahun-bulan-hari (contoh: 2025-11-14)
    java.text.SimpleDateFormat sdfTanggal = new java.text.SimpleDateFormat("yyyy-MM-dd");

    // Membuat formatter untuk waktu dengan format jam:menit (contoh: 14:30)
    java.text.SimpleDateFormat sdfWaktu = new java.text.SimpleDateFormat("HH:mm");

    // Mengambil nilai tanggal dari komponen jDateChooser lalu mengubahnya menjadi string sesuai format sdfTanggal
    String tanggal = sdfTanggal.format(jDateChooserTanggal.getDate());

    // Mengambil nilai waktu dari komponen spinner (spnWaktu) lalu mengubahnya menjadi string sesuai format sdfWaktu
    String waktu = sdfWaktu.format(spnWaktu.getValue());

    // Mengambil teks kegiatan dari field input txtKegiatan
    String kegiatan = txtKegiatan.getText();

    // Mengambil teks keterangan dari field input txtKeterangan
    String keterangan = txtKeterangan.getText();

    // Validasi: Jika tanggal atau kegiatan kosong, maka tampilkan peringatan ke pengguna
    if (tanggal.isEmpty() || kegiatan.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Tanggal dan kegiatan wajib diisi!", "Peringatan",
            javax.swing.JOptionPane.WARNING_MESSAGE);

        // Hentikan proses lebih lanjut (agar data tidak diproses)
        return;
    }


        // Update objek dataAgenda dengan nilai baru
        dataAgenda.setTanggal(tanggal);
        dataAgenda.setWaktu(waktu);
        dataAgenda.setKegiatan(kegiatan);
        dataAgenda.setKeterangan(keterangan);

        // Update ke database
        Model.agendaDAO dao = new Model.agendaDAO();
        dao.updateAgenda(dataAgenda);

        javax.swing.JOptionPane.showMessageDialog(this, "Agenda berhasil diperbarui!");
        dispose();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Terjadi kesalahan: " + e.getMessage(), "Error",
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

