    private void btnPdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPdfActionPerformed
        // TODO add your handling code here:
       javax.swing.JFileChooser chooser = new javax.swing.JFileChooser();
    chooser.setDialogTitle("Simpan Sebagai PDF");
    chooser.setSelectedFile(new java.io.File("AgendaPribadi.pdf"));
    int userSelection = chooser.showSaveDialog(this);

    if (userSelection == javax.swing.JFileChooser.APPROVE_OPTION) {
        try (java.io.FileOutputStream fos = new java.io.FileOutputStream(chooser.getSelectedFile())) {
            com.lowagie.text.Document document = new com.lowagie.text.Document();
            com.lowagie.text.pdf.PdfWriter.getInstance(document, fos);
            document.open();

            // Judul
            document.add(new com.lowagie.text.Paragraph("AGENDA PRIBADI",
                    new com.lowagie.text.Font(com.lowagie.text.Font.HELVETICA, 16, com.lowagie.text.Font.BOLD)));
            document.add(new com.lowagie.text.Paragraph("\n"));

            // Tabel
            com.lowagie.text.pdf.PdfPTable table = new com.lowagie.text.pdf.PdfPTable(model.getColumnCount() - 1);
            for (int i = 1; i < model.getColumnCount(); i++) {
                table.addCell(new com.lowagie.text.Phrase(model.getColumnName(i)));
            }

            for (int i = 0; i < model.getRowCount(); i++) {
                for (int j = 1; j < model.getColumnCount(); j++) {
                    Object value = model.getValueAt(i, j);
                    table.addCell(value != null ? value.toString() : "");
                }
            }

            document.add(table);
            document.close();

            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil disimpan sebagai PDF!");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Gagal membuat PDF: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnPdfActionPerformed

