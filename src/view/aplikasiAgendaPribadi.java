//    Method Tampilkan Agenda
    private void tampilkanAgenda() {
        try {
        model.setRowCount(0);

        // Jika checkbox "Tampilkan Semua" dicentang, tampilkan semua agenda
        if (chkTampilkanSemua.isSelected()) {
            List<agenda> semuaAgenda = controller.getAllAgenda();
            for (agenda a : semuaAgenda) {
                model.addRow(new Object[]{
                    a.getId(),
                    a.getTanggal(),
                    a.getWaktu(),
                    a.getKegiatan(),
                    a.getKeterangan()
                });
            }
            return;
        }

        // Jika tidak dicentang, tampilkan hanya bulan & tahun yang dipilih
        int tahun = jYearChooser1.getYear();
        int bulan = jMonthChooser1.getMonth() + 1;

        List<agenda> daftarAgenda = controller.getAllAgenda();
        for (agenda a : daftarAgenda) {
            String[] split = a.getTanggal().split("-");
            int thn = Integer.parseInt(split[0]);
            int bln = Integer.parseInt(split[1]);

            if (thn == tahun && bln == bulan) {
                model.addRow(new Object[]{
                    a.getId(),
                    a.getTanggal(),
                    a.getWaktu(),
                    a.getKegiatan(),
                    a.getKeterangan()
                });
            }
        }

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this,
                "Tidak ada agenda untuk bulan " + bulan + " tahun " + tahun);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }
}
    

    private void deleteAgenda() { 
         int selectedRow = tblAgenda.getSelectedRow();
    
    if (selectedRow != -1) {
        // Ambil nama kegiatan untuk ditampilkan dalam pesan konfirmasi
        String kegiatan = (String) model.getValueAt(selectedRow, 3);
        
        int konfirmasi = JOptionPane.showConfirmDialog(
            this,
            """
            Apakah Anda yakin ingin menghapus agenda berikut?
            
            Kegiatan: """ + kegiatan,
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );

        // Jika user menekan "Yes"
        if (konfirmasi == JOptionPane.YES_OPTION) {
            int id = (int) model.getValueAt(selectedRow, 0);
            try {
                controller.deleteAgenda(id);
                tampilkanAgenda();
                JOptionPane.showMessageDialog(this, "Agenda berhasil dihapus!");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus agenda: " + e.getMessage());
            }
        } else {
            // Jika user menekan "No"
            JOptionPane.showMessageDialog(this, "Penghapusan dibatalkan.");
        }

    } else {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus terlebih dahulu!");
    }
}


